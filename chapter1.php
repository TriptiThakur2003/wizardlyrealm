                <p>
                    The commercial contacts between India and Europe were very old via the land route either through the
                    Oxus valley or Syria or Egypt. With the discovery of the new sea route via the Cape of Good Hope by
                    Vasco da Gama in 1498, trade increased and many trading companies came to India and established
                    their trading centres. This is an important period in the history of India and is very crucial for the IAS
                    exam.
                    <br><br>
                    They started as traders but with the passage of time, in order to safeguard their commercial interests,
                    they aimed at dominating the politics of India. Thus, the commercial rivalry among the European
                    powers resulted in political rivalry and this not only brought them into conflict with each other but with
                    Indian rulers too. Ultimately, the British succeeded in establishing their rule in India.
                    <br><br>
                    <strong>Portuguese in India</strong><br>
                    In this section, you can read about the Portuguese rule in India that lasted for over 450 years. The
                    Portuguese were the first Europeans to arrive in India and the last to leave.
                    <br><br>
                    • In c. 1498 CE, Vasco da Gama of Portugal discovered a new sea route from Europe to India. He
                    sailed around Africa via the Cape of Good Hope and reached Calicut. <br>
                    • He was welcomed by the Zamorin, the Hindu ruler of Calicut and returned to Portugal in the next
                    year making huge profits from the Indian cargo that was worth 60 times the cost of his
                    expedition.<br>
                    • In c. 1500 CE, another Portuguese Pedro Alvares Cabral arrived in India and Vasco da Gama
                    also made a second trip in c. 1502 CE.<br>
                    • The Portuguese established trading settlements at Calicut, Cochin and Cannanore.<br>
                    • The first governor of the Portuguese in India was Francis de Almeida.<br>
                    • In c. 1509 CE, Afonso de Albuquerque was made the governor of the Portuguese territories in
                    India and in c. 1510 CE, he captured Goa from the ruler of Bijapur (during the reign of Sikander
                    Lodhi) and thereafter, Goa became the capital of the Portuguese settlements in India.<br>
                    • The Portuguese established their domination over the entire Asian coast from Hormuz in the
                    Persian Gulf to Malacca in Malaya and the spice islands in Indonesia. At the time of the death of
                    Afonso de Albuquerque, the Portuguese were the strongest naval power in India.<br>
                    • In c. 1530 CE, Nino da Cunha captured Diu and Bassein from Bahadur Shah of Gujarat. They
                    also established settlements at Salsette, Daman and Bombay on the west coast and at San Thome
                    near Madras and Hugli in Bengal on the east coast.<br>
                    • However, the Portuguese power declined in India by the end of the 16th century and they lost all
                    their acquired territories in India except Daman, Diu and Goa.<br>
                    <br><br>
                    <strong>Portuguese Contributions to India</strong><br>
                    • They brought tobacco cultivation to India. They established the first printing press at Goa in c.
                    1556 CE.<br>
                    • “The Indian Medicinal Plants” was the first scientific work which was published at Goa in c.
                    1563 CE.<br><br>
                    <strong>Causes of Decline of Portuguese in India</strong><br>
                    In this section, the causes for the Portuguese decline in India are discussed.<br>
                    1. The governors that succeeded Afonso de Albuquerque were weak and less competent which
                    eventually led to the decline of the Portuguese Empire in India.<br>
                    2. The Portuguese were intolerant and fanatical in religious matters. They indulged in forcible
                    conversion of the native people to Christianity. Their approach in this respect was hateful to the
                    people of India where religious tolerance was the rule.<br>
                    3. The Portuguese administration was more interested in making fortunes for themselves which
                    resulted in the further alienation of the people of India. They were also involved in inhuman
                    cruelties and lawlessness. They did not even shy away from piracy and plunder. All these acts
                    resulted in a hostile attitude towards the Portuguese.<br>
                    4. The Portuguese and the Spanish had left the English and the Dutch far behind during the 15th
                    century and the first half of the 16th century. But in the later half of the 16th century, England
                    and Holland, and later, France, all growing commercial and naval powers, waged a fierce
                    struggle against the Spanish and the Portuguese monopoly of world trade. In this struggle, the
                    latter were subjugated. This also weakened their power in India.<br>
                    5. Also, the might of the Mughal Empire and the growing power of the Marathas did not let the
                    Portuguese maintain their trade monopoly for long in India. For instance, they clashed with the
                    Mughal power in Bengal in c. 1631 CE and were driven out of their settlement at Hughli.
                    6. The Portuguese discovered Brazil in Latin America and began to pay much more attention to it
                    than its territories in India.<br>
                    7. When Portugal came under Spain in c. 1580 CE, the Spanish interests predominated over the
                    Portugal interests which were subsequently side lined.<br>
                    <br>
                    <strong>Dutch in India</strong><br>
                    The Dutch East India Company was established in c. 1602 CE under the name Vereenigde Oost Indische
                    Compagnie (VOC). Dutch set up their first factory at Masulipatnam in Andhra. They also established
                    trading depots at Surat, Broach, Cambay and Ahmedabad in Gujarat in west India, Cochin in Kerala,
                    Chinsura in Bengal, Patna in Bihar and Agra in U.P. Pulicat (Tamil Nadu) was their main centre in India
                    and later, it was replaced by Nagapattinam. In the 17th century, they won over the Portuguese and
                    emerged the most dominant power in European trade in the East. They dislodged the Portuguese fromthe Malay straits and the Indonesian islands, and in c. 1623 defeated English attempts to establish
                    themselves there. The Anglo-Dutch rivalry continued for about seven years during which the Dutch lost
                    their settlements to the British one by one and finally, the Dutch were defeated by the English in the
                    Battle of Bedara in c. 1759.<br><br>
                    <strong>British in India</strong><br>
                    The English Association or Company to trade with the East was formed in c. 1599 CE under the
                    auspices of a group of merchants known as “The merchant Adventurers”. The company was given a
                    royal charter and the exclusive privilege to trade in the East by Queen Elizabeth on 31st December
                    c.1600 CE and was popularly known as the East India Company.<br><br>
                    • In c. 1609 CE, Captain William Hawkins arrived at the court of Mughal Emperor Jahangir to
                    seek permission to establish an English trading centre at Surat.<br>
                    • But it was refused by the Emperor due to pressure from the Portuguese.<br>
                    • Later in c. 1612 CE, Jahangir permitted the East India Company to set up a factory at Surat.<br>
                    • In c. 1615 CE, Sir Thomas Roe came to the Mughal court as ambassador of James Ⅰ, the king of
                    England and succeeded in getting an Imperial farman to trade and establish factories in different
                    parts of India.<br>
                    • Thus, by c. 1619 CE, the English established their factories at Agra, Ahmedabad, Baroda and
                    Broach.<br>
                    • The English opened their first factory in the south at Masulipatnam.<br>
                    • In c. 1639 CE, Francis Day obtained the site of Madras from the Raja of Chandragiri and built a
                    small fort around their factory called Fort St. George. <br>
                    • Madras soon replaced Masulipatnam as the headquarters of the English on the Coromandel coast.<br>
                    • The English East India Company acquired Bombay from Charles Ⅱ, the then king of England in
                    c. 1668 CE and Bombay became the headquarters of the company on the west coast.<br>
                    • In c. 1690 CE, an English factory was established at a place called Sutanuti by Job Charnock.
                    Later, it developed into the city of Calcutta where Fort William was built and which later became
                    the capital of British India.<br>
                    • British settlements in Madras, Bombay and Calcutta became the nuclei of flourishing cities.<br>
                    • The British East India Company grew in power and tended to acquire the status of a sovereign
                    state in India.<br><br>

                    <strong>French in India</strong><br>
                    The French East India Company was founded in c. 1664 CE by Colbert, a minister under Louis ⅩⅣ. In
                    c. 1668 CE, the first French factory was set up at Surat by Francis Caron. In c. 1669 CE, Maracara established a factory at Masulipatnam. In c. 1673 CE, Francois Martin founded Pondicherry (Fort
                    Louis), which became the headquarters of the French possessions in India and he became its first
                    governor. In c. 1690 CE, the French acquired Chandranagore near Calcutta from the governor, Shaista
                    Khan. The French established their factories at Balasore, Mahe, Qasim Bazar and Karaikal. The arrival
                    of Joseph François Dupleix as French governor in India in c. 1742 CE saw the beginning of the Anglo-French conflict which resulted in the famous Carnatic wars.
